public class goods {
    //定义属性
    int id;
    String name;
    double weight;
    double price;
    //get和set方法
    public double getPrice() {
        return price;
    }

    public int getId() {
        return id;
    }

    public double getWeight() {
        return weight;
    }

    public String getName() {
        return name;
    }
}
